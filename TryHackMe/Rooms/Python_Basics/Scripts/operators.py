# Conditional statements
if( 1 == 1):
    print("1 == 1")
if(1 > 0):
    print("1 > 0")
if(1 < 10):
    print("1 < 10")
if(1 != 0):
    print("1 != 0")
if(1 >= 0):
    print("1 > 0 but 1!= 0")
if(1 <= 10):
    print("1 < 10 but 1!= 10")
